#   data-types in python

x = "hello, world"                                          # string
x = 20                                                      # int
x = 20.4                                                    # float
x = 2 + 2j                                                  # complex
x = ["apple", "banana", "mango"]                            # list
x = ("apple", "banana", "mango")                            # tuple
x = {"apple": 0, "mango": 1, "banana": 2}                   # dictionary
x = {"apple", "banana", "mango"}                            # set
x = frozenset({"apple", "banana", "mango"})                 # frozenset
x = True                                                    # boolean

#   Specifying data-type to a variable

x = str("hello, world")                                     # string
x = int(20)                                                 # integer
x = float(20.40)                                            # float
x = complex(2+4j)                                           # complex
x = list(("apple", "banana", "mango"))                      # list
x = tuple(("apple", "banana", "mango"))                     # tuple
x = dict(apple=0, banana=1, mango=2)                        # dictionary
x = set(("apple", "banana", "mango"))                       # set
x = frozenset(("apple", "banana", "mango"))                 # frozenset
